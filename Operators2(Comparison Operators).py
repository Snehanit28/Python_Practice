# Comparison Operators: Used to compare two values and return a boolean result (True or False).

"""Comparison Operators:
     ==  -> Equal to
     !=  -> Not equal to
     >   -> Greater than
     <   -> Less than
     >=  -> Greater than or equal to
     <=  -> Less than or equal to"""

x = 10
y = 20  
print(x == y)  # => False
print(x != y)  # => True   
print(x > y)   # => False
print(x < y)   # => True
print(x >= y)  # => False
print(x <= y)  # => True



# Data types diffine the type of which are stored
    
"""Numeric data types 
     int -> Whole numers (like- 100,0,-100)
     float -> Decimal numbers (like- 0.678, 6.5476, -12.67879)
     complex -> real & imaginary part (like- a+bj,  3+4j) """


a = 4
b = 48.465
c = 5+3j 

print(a,b,c)   # => 4 48.465 (5+3j)
print(type(a),type(b),type(c))     # => <class 'int'> <class 'float'> <class 'complex'>


